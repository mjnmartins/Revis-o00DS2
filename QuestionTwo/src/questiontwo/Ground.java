
package questiontwo;


public class Ground {
    private float length;
    private float height;

    public Ground() {
        this.length = 0;
        this.height = 0;
    }

    public float getLength() {
        return length;
    }

    public void setLength(float length) {
        this.length = length;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }
    
}
