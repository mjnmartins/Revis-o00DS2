package questionone;

public class ExchangeWords {

    private String phrase;
    private String word;

    public ExchangeWords() {
        this.phrase = "";
        this.word = "";
    }

    public String getPhrase() {
        return phrase;
    }

    public void setPhrase(String phrase) {
        this.phrase = phrase;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

}
